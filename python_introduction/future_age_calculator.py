# future_age_calculator.py

# Step 1: Prompt the user for their current age
current_age = int(input("How old are you? "))  # Get user input and convert it to an integer

# Step 2: Calculate the age in 2050
age_in_2050 = current_age + 27  # Add 27 years to current age

# Step 3: Print the result
print(f"In 2050, you will be {age_in_2050} years old.")
